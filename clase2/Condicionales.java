public class Condicionales {

    public static void main(String[] args) {

        // Condicionales
        int numero = -5;

        if (numero > 0) {
            System.out.println("El número es positivo");
        } else if (numero < 0) {
            System.out.println("El número es negativo");
        } else {
            System.out.println("El número es cero");
        }
    
    } 
}
