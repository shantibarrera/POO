public class Bucles {

    public static void main(String[] args) {
        
        // For (inicio; condición de parada; incremento o decremento) 
        for (int i = 0; i <= 5; i++) {
            System.out.println("Número de iteración: " + i);
        }

        System.out.println(" Espacio entre iteraciones");

        // Estructura while
        int j = 1;
        while (j <= 5) {
            System.out.println("Número de iteración J: " + j);
            j++;
        }
    }
}